function add (a, b){
    return a + b;
}
function subtract(a ,b){
    return a - b;
}
function multiply(a, b){
    return a * b;
}
function divide(a, b){
    return a / b ;
}
function increment (n){
    let a = ++n;
    return a ;
}
function decrement(n) {
    let a = --n;
    return a ;
}
function makeInt(String) {
    return parseInt(String,10);
}
function preserveDecimal(String){
    return parseFloat(String)
}
